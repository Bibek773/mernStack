// what is destructor?
// Destructuring assignment is a JavaScript expression that makes it possible to unpack values from arrays, or properties from objects, into distinct variables.
//Array Destructor
let [a, b, c] = [10, 22, 44]

console.log(a);
console.log(b);
console.log(c);
console.log("-----------------------------------------------------");

// object destructor 
// in object order doesn't matter 
let { weight, name, isMarried} = { name: "bibek", weight :4334, isMarried : true}

console.log(name);
console.log(weight);
console.log(isMarried);
console.log("-----------------------------------------------------");
